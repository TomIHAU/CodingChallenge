const { Model, DataTypes } = require("sequelize");
const sequelize = require("../config/connection");

class Packaging extends Model {}

Packaging.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    product_id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      references: {
        model: "products",
        key: "id",
      },
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: "packaging",
  }
);

module.exports = Packaging;
